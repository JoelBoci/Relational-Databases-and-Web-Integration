    Poppleton is a village and civil parish in the unitary authority of the City of York in North Yorkshire, England. 

    It is situated by the west bank of the River Ouse adjacent to Nether Poppleton, and west of York close to the A59 from York to Harrogate.

    In 972, the village was recorded as "Popeltun" in a list made for Archbishop of York Oswald of Church property lost in the wars earlier in the century, and in the Domesday Book as "Popletune". 
    The villages and lands were given by Osbern De Arches to the Abbot of St Mary's in York. It was, therefore, under the ecclesiastical rule of the Parish of St Mary-Bishophill Junior.

    During the reign of Richard II, the village was the scene of the murder of a mayor of York.

    In 1644, the 25,000-strong Scottish and Parliament armies, led by the Earl of Manchester, laid siege to the city of York. To facilitate communications, they built a "bridge of boats" at Poppleton. 
    This bridge was eventually taken by Prince Rupert and his Royalist forces, but he subsequently lost the battle at Marston Moor.

    The village benefited from the growth in the railways in the 19th century when the York, Knaresborough and Harrogate Railway routed its line through Poppleton and built a station.

    On 22 January 1876, the village became the birthplace of Flora Sandes, the only woman to be officially enlisted during the First World War.

    The village was historically part of the West Riding of Yorkshire until 1974. It was then a part of the Borough of Harrogate in North Yorkshire from 1974 until 1996. 
    Since 1996 it has been part of the City of York unitary authority

